const add = (x, y) => {
    return x + y
}

const subtract = (x, y) => {
    return x - y
}

const multiply = (x, y) => {
    return x * y
}

const divide = (x, y) => {
    return x / y
}

const increment = (x) => {
    return ++x
}

const decrement = (x) => {
    return --x
}

const makeInt = (x) => {
    return parseInt(x, 10)
}

const preserveDecimal = (x) => {
    return parseFloat(x)
}